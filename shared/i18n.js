// Global click dismiss handler for languages dropdown panel
document.addEventListener('click', (event) => {
    const panel = document.getElementById('dd-lang-panel');
    const trigger = document.getElementById('dd-lang-btn');
    const chevron = document.getElementById('dd-lang-chevron');
    if (!panel || panel.classList.contains('hidden')) return;

    if (!trigger.contains(event.target) && !panel.contains(event.target)) {
        panel.classList.add('hidden');
        if (chevron) chevron.style.transform = 'rotate(0deg)';
    }
});

